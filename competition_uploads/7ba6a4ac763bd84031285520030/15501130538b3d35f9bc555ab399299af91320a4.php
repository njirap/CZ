<div id="problem-statement">
<h5>Problem</h5>
<p> You are required to find whether a given number is divisible by 7
</p>
<h5>Input</h5>
<p>The input file starts with the number of test cases <b>T</b> on a new line. The next lines consists of exactly one number each <b>S</b>
</p>
<h6>Example</h6>
<div class="sample-data">
<code>
<pre>
3
7
0
13
</pre>
</code>
</div>
<h5>Output</h5>
<p>For each test case, output one line in the following format &quot;<b>#Y: X </b> &quot; (NB: quotes for clarity) where Y is case number beginning from 1 and X is either <b>true</b> or <b>false</b> followed by a new line.<br />
<b>NOTE</b><i> There should be exactly one space between the colon and X</i>
</p>
<h6>Example</h6>
<div class="sample-data">
<code>
<pre>
#1: true
#2: true
#3: false
</pre>
</code>
</div>

</div>
<h5>Limits</h5>
<p>
1&lt;<b>T</b>&lt;1000<br />
0&lt;<b>S</b>&lt;2<sup>30</sup>
</p>